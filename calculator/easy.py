def addition (arg1, arg2):
  x = arg1 + arg2
  return x
  #========================================#
  # input: integer arg1, integer arg2      #
  # return: integer/ string                #
  #========================================#

def substraction (arg1, arg2):
  x = arg1 - arg2
  return x
  #========================================#
  # input: integer arg1, integer arg2      #
  # return: integer/ string                #
  #========================================#

def multiplication (arg1, arg2):
  #========================================#
  # input: integer arg1, integer arg2      #
  # return: integer/ string                #
  #========================================#
  x = arg1*arg2
  return x

def division (arg1, arg2):
  #========================================#
  # input: integer arg1, integer arg2      #
  # return: integer/ string                #
  #========================================#
  x = arg1/arg2
  return x
